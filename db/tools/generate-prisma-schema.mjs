// ONE-TIME BOOTSTRAP — kept for provenance, do not re-run.
//
// This generated apps/api/prisma/schema.prisma from the restored SQL Server catalog
// (db/columns.psv) plus the validated relationships (db/foreign-keys.psv). It is how the
// schema came to exist and what justifies every departure from the legacy DDL.
//
// From here schema.prisma is the source of truth and evolves through Prisma migrations.
// Re-running this would discard anything added since (e.g. the job-alerts table, which
// has no legacy equivalent).
//
import { readFileSync, writeFileSync } from 'node:fs';

const R = (p) => readFileSync(p, 'utf8').split('\n').filter(Boolean);
const cols = R('db/columns.psv').map((l) => {
  const [table, col, type, maxLen, prec, scale, nullable, identity, ord, pk] = l.split('|');
  return { table, col, type, maxLen: +maxLen, prec: +prec, scale: +scale,
           nullable: nullable === '1', identity: identity === '1', ord: +ord, isPk: pk !== '' };
});
const fks = R('db/foreign-keys.psv').map((l) => {
  const [ct, cc, pt, pc, conf] = l.split('|');
  return { ct, cc, pt, pc, conf };
});
const dead = new Set(R('db/dead-tables.txt').map((s) => s.trim()));

// Tables the legacy DB left without a primary key. We are designing a new schema, not
// mirroring an old one, so each gets a real key. Every choice below was verified unique
// against the restored data.
const PK_OVERRIDE = {
  tblSubscriberTags: ['SubscriberID', 'TagID'],
  tblSubscriberPrefferedLocations: ['SubscriberID', 'CityID'],
  tblSecActiveSessions: ['RowID'],
  tblClientJobs_EducationType: ['JobID', 'EducationTypeID'],
  tblClientJobs_Gendermapping: ['JobID', 'Gender'],
  tblSubscriberJobStatusLatest: ['SubscriberID'],
  tblSubscriberCVDetails: ['SubscriberID'],
  tblPMstNodeTypes: ['NodeType'],
};

// Surrogate keys the legacy DB never declared as IDENTITY, so the app had to supply the
// value itself. They are generated by the database now.
const AUTOINCREMENT = new Set([
  'tblSecActiveSessions.RowID',
]);

// Deliberate departures from the legacy column definition.
const COLUMN_OVERRIDE = {
  // Legacy stored the password in PLAINTEXT, so varchar(50) was enough. An Argon2id hash
  // is ~95+ chars and will not fit.
  'tblSecUser.Password': { type: 'nvarchar', maxLen: 510 },
};

const model = (t) => t.replace(/^tbl/, '').replace(/[^A-Za-z0-9]/g, '_');
const field = (c) => {
  const f = c.charAt(0).toLowerCase() + c.slice(1);
  return /^[0-9]/.test(f) ? '_' + f : f;
};

function prismaType(c, isPkCol = false) {
  // A key column cannot be nullable. The legacy DB left several of them nullable simply
  // because it declared no primary key at all; we are fixing that, not preserving it.
  const n = c.nullable && !isPkCol ? '?' : '';
  switch (c.type) {
    case 'bigint': return [`BigInt${n}`, ''];
    case 'int': return [`Int${n}`, ''];
    case 'smallint': return [`Int${n}`, ' @db.SmallInt'];
    case 'tinyint': return [`Int${n}`, ' @db.SmallInt'];
    case 'bit': return [`Boolean${n}`, ''];
    case 'float': return [`Float${n}`, ''];
    case 'real': return [`Float${n}`, ' @db.Real'];
    case 'decimal': case 'numeric': return [`Decimal${n}`, ` @db.Decimal(${c.prec}, ${c.scale})`];
    case 'money': return [`Decimal${n}`, ' @db.Money'];
    case 'datetime': case 'datetime2': case 'smalldatetime':
      return [`DateTime${n}`, ' @db.Timestamp(6)'];
    case 'date': return [`DateTime${n}`, ' @db.Date'];
    case 'time': return [`DateTime${n}`, ' @db.Time(6)'];
    case 'uniqueidentifier': return [`String${n}`, ' @db.Uuid'];
    case 'text': case 'ntext': return [`String${n}`, ' @db.Text'];
    case 'varbinary': case 'binary': case 'image': return [`Bytes${n}`, ''];
    case 'char': case 'nchar': {
      const len = c.type === 'nchar' ? c.maxLen / 2 : c.maxLen;
      return [`String${n}`, ` @db.Char(${len})`];
    }
    case 'varchar': case 'nvarchar': {
      if (c.maxLen === -1) return [`String${n}`, ' @db.Text'];
      const len = c.type === 'nvarchar' ? c.maxLen / 2 : c.maxLen;
      return [`String${n}`, ` @db.VarChar(${len})`];
    }
    default: return [`String${n}`, ` // TODO unmapped SQL type: ${c.type}`];
  }
}

const tables = [...new Set(cols.map((c) => c.table))].filter((t) => !dead.has(t)).sort();
const liveFks = fks.filter((f) => !dead.has(f.ct) && !dead.has(f.pt) && tables.includes(f.ct) && tables.includes(f.pt));

// back-relation names must be unique per parent model
const backNames = new Map();
for (const f of liveFks) {
  const key = `${f.pt}`;
  const arr = backNames.get(key) ?? [];
  arr.push(f);
  backNames.set(key, arr);
}
const relName = (f) => {
  const siblings = liveFks.filter((x) => x.ct === f.ct && x.pt === f.pt);
  return siblings.length > 1 ? `${model(f.ct)}_${f.cc}_To_${model(f.pt)}` : `${model(f.ct)}To${model(f.pt)}`;
};

let out = `// Aajiveka — PostgreSQL schema
//
// Derived from a real restore of db_aajiveka.bak, NOT inferred. Column types,
// nullability and identity come straight from the SQL Server catalog.
//
// The legacy database declared ZERO foreign keys and left 36 of 73 tables without a
// primary key. None of that is mirrored here. Every relation below was derived from the
// JOIN clauses in db/procs/ and then VALIDATED against the real data with an orphan
// check — see db/foreign-keys.psv for the evidence and confidence of each one.
//
// 13 dead tables (no rows, referenced by no procedure) are omitted — see db/dead-tables.txt.

generator client {
  provider = "prisma-client-js"
}

datasource db {
  provider = "postgresql"
}

`;

for (const t of tables) {
  const tc = cols.filter((c) => c.table === t).sort((a, b) => a.ord - b.ord);
  const declaredPk = tc.filter((c) => c.isPk).map((c) => c.col);
  // The legacy DB left 36 tables without a PK even where an IDENTITY column obviously
  // served as one. Promote that column rather than carry the omission forward.
  const identityPk = tc.filter((c) => c.identity).map((c) => c.col);
  const pk = declaredPk.length ? declaredPk : (PK_OVERRIDE[t] ?? identityPk);
  const outFks = liveFks.filter((f) => f.ct === t);
  const inFks = liveFks.filter((f) => f.pt === t);

  out += `model ${model(t)} {\n`;
  for (const c of tc) {
    const isPkCol = pk.includes(c.col);
    const fk = outFks.find((f) => f.cc === c.col);
    // Legacy declared no FKs, so nobody noticed that some child columns are narrower than
    // the key they point at (e.g. tblSecUserLogin.UserID int -> tblSecUser.UserID bigint).
    // Adopt the parent's type so the relation is actually declarable.
    const parentPk = fk && cols.find((x) => x.table === fk.pt && x.col === fk.pc);
    let effective = parentPk && parentPk.type !== c.type
      ? { ...c, type: parentPk.type, maxLen: parentPk.maxLen, prec: parentPk.prec, scale: parentPk.scale }
      : c;
    const ov = COLUMN_OVERRIDE[`${t}.${c.col}`];
    if (ov) effective = { ...effective, ...ov };
    // A column where the legacy uses 0 as a "no value" sentinel, or that carries orphans,
    // becomes NULL on migration — so it has to be nullable here even if the source said
    // NOT NULL. Otherwise the row cannot be loaded at all.
    if (fk && fk.conf !== 'confirmed' && !isPkCol) effective = { ...effective, nullable: true };
    const [ty, attr] = prismaType(effective, isPkCol);
    const bits = [];
    if (pk.length === 1 && pk[0] === c.col) bits.push('@id');
    if (c.identity || AUTOINCREMENT.has(`${t}.${c.col}`)) bits.push('@default(autoincrement())');
    // A relation target must be unique. Some are not PKs (e.g. SecUser.NodeID).
    if (!isPkCol && liveFks.some((f) => f.pt === t && f.pc === c.col)) bits.push('@unique');
    bits.push(`@map("${c.col}")`);
    const widened = parentPk && parentPk.type !== c.type ? ` (widened from ${c.type})` : '';
    const note = fk ? `  // FK -> ${model(fk.pt)} (${fk.conf})${widened}` : '';
    out += `  ${field(c.col).padEnd(28)} ${(ty + attr).padEnd(34)} ${bits.join(' ')}${note}\n`;
  }
  if (outFks.length || inFks.length) out += '\n';
  for (const f of outFks) {
    const c = tc.find((x) => x.col === f.cc);
    const opt = ((c?.nullable || f.conf !== "confirmed") && !pk.includes(f.cc)) ? "?" : "";
    out += `  ${field(f.cc).replace(/Id$/i, '').padEnd(28)} ${(model(f.pt) + opt).padEnd(34)} @relation("${relName(f)}", fields: [${field(f.cc)}], references: [${field(f.pc)}])\n`;
  }
  for (const f of inFks) {
    const name = `${field(f.ct).replace(/^tbl/, '')}${liveFks.filter((x) => x.ct === f.ct && x.pt === f.pt).length > 1 ? '_' + f.cc : ''}`;
    // If the child's FK column IS its primary key, the relation is one-to-one, not one-to-many.
    // e.g. SubscriberCVDetails.SubscriberID is that table's PK, so a subscriber has at most
    // one CV — modelling it as a list would be wrong.
    const childCols = cols.filter((c) => c.table === f.ct);
    const childPk = childCols.filter((c) => c.isPk).map((c) => c.col);
    const childKey = childPk.length ? childPk : (PK_OVERRIDE[f.ct] ?? childCols.filter((c) => c.identity).map((c) => c.col));
    const oneToOne = childKey.length === 1 && childKey[0] === f.cc;
    out += `  ${name.padEnd(28)} ${(model(f.ct) + (oneToOne ? '?' : '[]')).padEnd(34)} @relation("${relName(f)}")\n`;
  }
  if (pk.length > 1) out += `\n  @@id([${pk.map(field).join(', ')}])\n`;
  if (!pk.length) out += `\n  // TODO: no primary key could be established for this table\n  @@ignore\n`;
  out += `  @@map("${t}")\n}\n\n`;
}

writeFileSync('apps/api/prisma/schema.prisma', out);
console.log(`models: ${tables.length}`);
console.log(`relations: ${liveFks.length}`);
console.log(`dead tables omitted: ${dead.size}`);
const noPk = tables.filter((t) => !cols.some((c) => c.table === t && (c.isPk || c.identity)) && !PK_OVERRIDE[t]);
console.log(`models still without a PK: ${noPk.length}${noPk.length ? ' -> ' + noPk.join(', ') : ''}`);
